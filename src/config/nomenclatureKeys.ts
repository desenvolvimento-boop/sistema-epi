/** Ordem de exibição no dicionário de termos (alinhado com nomenclature.config.js) */
export const NOMENCLATURE_GROUP_ORDER = [
  'Menu',
  'Páginas',
  'Relatórios',
  'Escopos',
  'Entidades',
  'Ações',
  'Mensagens',
] as const;

export function compareNomenclatureGroups(a: string, b: string): number {
  const index = (group: string) => {
    const i = (NOMENCLATURE_GROUP_ORDER as readonly string[]).indexOf(group);
    return i === -1 ? NOMENCLATURE_GROUP_ORDER.length : i;
  };
  const byOrder = index(a) - index(b);
  if (byOrder !== 0) return byOrder;
  return a.localeCompare(b, 'pt-BR');
}
